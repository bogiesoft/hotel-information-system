<?php

class MRoomTypeController extends GxController {
    public $baseName = "Room Type";
    public $actionName = "List Data";
    


    public function actionView($id) {
        $this->render('view', array(
            'model' => $this->loadModel($id, 'MRoomType'),
        ));
    }

    public function actionCreate() {
        $this->actionName = "New Data";
        $model = new MRoomType;


        if (isset($_POST['MRoomType'])) {
            $model->setAttributes($_POST['MRoomType']);

            if ($model->save()) {
                Yii::app()->user->setFlash('success', "Data saved!");
                $this->redirect(array('index'));
            }
        }

        $this->render('create', array( 'model' => $model));
    }

    public function actionUpdate($id) {
        $this->actionName = "Update Data";
        $model = $this->loadModel($id, 'MRoomType');


        if (isset($_POST['MRoomType'])) {
            $model->setAttributes($_POST['MRoomType']);

            if ($model->save()) {
                Yii::app()->user->setFlash('success', "Data saved!");
                $this->redirect(array('index'));
            }
        }

        $this->render('update', array(
            'model' => $model,
        ));
    }

    public function actionDelete($id) {
        MRoomType::model()->deleteByPk($id);
        Yii::app()->user->setFlash('success', "Data deleted.");
        $this->redirect(array('index'));
    }

    public function actionIndex() {
        $this->render('index');
    }
}