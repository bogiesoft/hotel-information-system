<?php

class MReligionController extends GxController {
    public $baseName = "Religion";
    public $actionName = "List Data";
    


    public function actionView($id) {
        $this->render('view', array(
            'model' => $this->loadModel($id, 'MReligion'),
        ));
    }

    public function actionCreate() {
        $this->actionName = "New Data";
        $model = new MReligion;


        if (isset($_POST['MReligion'])) {
            $model->setAttributes($_POST['MReligion']);

            if ($model->save()) {
                Yii::app()->user->setFlash('success', "Data saved!");
                $this->redirect(array('index'));
            }
        }

        $this->render('create', array( 'model' => $model));
    }

    public function actionUpdate($id) {
        $this->actionName = "Update Data";
        $model = $this->loadModel($id, 'MReligion');


        if (isset($_POST['MReligion'])) {
            $model->setAttributes($_POST['MReligion']);

            if ($model->save()) {
                Yii::app()->user->setFlash('success', "Data saved!");
                $this->redirect(array('index'));
            }
        }

        $this->render('update', array(
            'model' => $model,
        ));
    }

    public function actionDelete($id) {
        MReligion::model()->deleteByPk($id);
        Yii::app()->user->setFlash('success', "Data deleted.");
        $this->redirect(array('index'));
    }

    public function actionIndex() {
        $this->render('index');
    }
}